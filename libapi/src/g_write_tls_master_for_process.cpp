#include "ghost/user.h"

/**
 *
 */
uint8_t g_write_tls_master_for_process(g_process_creation_identifier process, uint8_t* content, uint32_t copysize, uint32_t totalsize, uint32_t alignment) {
	g_syscall_write_tls_master_for_process data;
	data.processObject = process;
	data.content = content;
	data.copysize = copysize;
	data.totalsize = totalsize;
	data.alignment = alignment;
	g_syscall(G_SYSCALL_WRITE_TLS_MASTER_FOR_PROCESS, (uint32_t) &data);
	return data.result;
}
