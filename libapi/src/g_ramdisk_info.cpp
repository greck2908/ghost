#include "ghost/user.h"

/**
 *
 */
void g_ramdisk_info(int node, g_ramdisk_entry_info* out) {
	g_syscall_ramdisk_info data;
	data.nodeId = node;
	g_syscall(G_SYSCALL_RAMDISK_INFO, (uint32_t) &data);

	// copy name
	char* src = data.name;
	char* dest = out->name;
	while (*src) {
		*dest++ = *src++;
	}
	*dest = 0;

	out->length = data.length;
	out->type = data.type;
}
